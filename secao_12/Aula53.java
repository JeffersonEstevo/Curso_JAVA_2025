// Operadores de Comparação

/* 
OPERADORES DE COMPARAÇÃO

Os operadores aritméticos são usados para comparar dois valores:
    • Igual a (==)
    • Diferente de  (!=)
    • Maior que (>)
    • Menor que (<)
    • Maior ou igual a (>=)
    • Menor ou igual a (<=)

*/

public class Aula53 {
    public static void main(String[] args) {
       int a = 10;
       int b = 20;

       System.out.println(a == b); // Saída: false
       System.out.println(a != b); // Saída: true
       System.out.println(a > b); // Saída: false
       System.out.println(a < b); // Saída: true
       System.out.println(a >= b); // Saída: false
       System.out.println(a <= b); // Saída: true

    }
}
